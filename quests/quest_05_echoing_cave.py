# Quest 5: The Echoing Cave
# Concept: Reassigning variables - the value can be changed.

player_health = 100
print(f"Starting health: {player_health}")

# A monster attacks! Subtract 25
player_health = player_health - 25
print(f"After monster attack: {player_health}")

# The player finds a potion! Add 10
player_health = player_health + 10
print(f"After potion: {player_health}")

print(f"Final health: {player_health}")
